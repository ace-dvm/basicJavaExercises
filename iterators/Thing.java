public class Thing
{
    private int number;

    /**
     * Constructor for objects of class Thing
     */
    public Thing(){
    }

    /**
     * Sets the number of this Thing
     * 
     * @param int n a number
     */
    public void setNumber(int n){
        number = n;
    }
    
    /**
     * Gets the number of this Thing
     * @return int number of this thing
     */
    public int getNumber(){
        return number;
    }
}
